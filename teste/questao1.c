//Questão 01) O que é e para que serve um ponteiro?
//É uma variável que armazena o endereço de memória de outra variável. 
// Ou seja, ela aponta para o local na memória onde o valor está alocado. 
// Dessa forma, sua funcionalidade pode ser enxergada no acesso e manipulação direta da memória, 
// o que facilita a passagem eficiente de parâmetros para funções – evitando cópias desnecessárias. 
// Como também, na manipulação de arrays e strings, ou até mesmo na implementação de estruturas 
